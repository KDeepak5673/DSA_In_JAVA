public class Main {
    public static void main(String[] args) {
        // String

        String str = "Deepak";

        System.out.println(str);
        //String - It is a Class in Java
        //str - Ref. Variable
        //"Deepak" - Object

        //String is immutable in java

        str.concat(" Kumar");

        System.out.println(str);

        //to concat we have to explicitly assign string

        str = str.concat(" Kumar");

        System.out.println(str);





    }





}